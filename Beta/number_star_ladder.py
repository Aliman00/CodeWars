OUTPUT = '1{}{}'.format


def pattern(n):
    return '\n'.join(OUTPUT('*' * a, a + 1 if a else '') for a in xrange(n))


assert pattern(3) == "1\n1*2\n1**3"
assert pattern(7) == "1\n1*2\n1**3\n1***4\n1****5\n1*****6\n1******7"
assert pattern(20) \
    == "1\n1*2\n1**3\n1***4\n1****5\n1*****6\n1******7\n1*******8\n" \
       "1********9\n1*********10\n1**********11\n1***********12\n" \
       "1************13\n1*************14\n1**************15\n" \
       "1***************16\n1****************17\n1*****************18\n" \
       "1******************19\n1*******************20"
