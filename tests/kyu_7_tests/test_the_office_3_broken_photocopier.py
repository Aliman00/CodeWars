import unittest

from katas.kyu_7.the_office_3_broken_photocopier import broken


class BrokenTestCase(unittest.TestCase):
    def test_equal_1(self):
        self.assertEqual(broken('001111110011001110101111101001'),
                         '110000001100110001010000010110')

    def test_equal_2(self):
        self.assertEqual(broken('00110100110010011100110010100111100000011'),
                         '11001011001101100011001101011000011111100')

    def test_equal_3(self):
        self.assertEqual(broken(
            '01001000010010011110100010010110111011100101101011100001110100'
            '101110001110010111'),
            '10110111101101100001011101101001000100011010010100011110001011'
            '010001110001101000')

    def test_equal_4(self):
        self.assertEqual(broken(
            '00010011011010110000100110011000010011011000101111011000101'),
            '11101100100101001111011001100111101100100111010000100111010')

    def test_equal_5(self):
        self.assertEqual(broken('00000011111010001100010101001110'),
                         '11111100000101110011101010110001')

    def test_equal_6(self):
        self.assertEqual(broken(
            '00100011101110111010111010101100001111000100001010110010110000'
            '00010'),
            '11011100010001000101000101010011110000111011110101001101001111'
            '11101')

    def test_equal_7(self):
        self.assertEqual(broken(
            '0101100110010010111110101100100100010110100000111011101000110'),
            '1010011001101101000001010011011011101001011111000100010111001')

    def test_equal_8(self):
        self.assertEqual(broken(
            '10101001000001101001110101011111111000110100110001010101100110'
            '0000111000101101000110010000'),
            '01010110111110010110001010100000000111001011001110101010011001'
            '1111000111010010111001101111')

    def test_equal_9(self):
        self.assertEqual(broken(
            '01000101110011101110100101101110100001101111010001010011110000'
            '01011011111010010001'),
            '10111010001100010001011010010001011110010000101110101100001111'
            '10100100000101101110')

    def test_equal_10(self):
        self.assertEqual(broken('100011101111'), '011100010000')
